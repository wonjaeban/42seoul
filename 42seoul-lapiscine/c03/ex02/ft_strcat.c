char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	index;

	i = 0;
	while (dest[i])
	{
		i++;
	}
	index = i;
	i = 0;
	while (src[i])
	{
		dest[index] = src[i];
		i++;
		index++;
	}
	dest[index] = '\0';
}
