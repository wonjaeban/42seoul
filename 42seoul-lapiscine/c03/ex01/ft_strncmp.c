int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	i;

	i = 0;
	while (i <= n)
	{
		if (s2[i] > s1[i])
			return (-1);
		else if (s2[i] < s1[i])
			return (1);
		i++;
	}
	return (0);
}
